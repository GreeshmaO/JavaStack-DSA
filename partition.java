public class partition {
    public static void main(String[] args) {
        int[]nums={1,3,4,2};
        int n=nums.length;
        int sum;
        for(int i=0;i<n-1;i++){
            for(int j=1;j<n;j++){
                 sum=i+j;
            }
    }
    System.out.println(sum);
    
}
}
